// Program to accept any 10 numbers and display their sum.

#include <iostream>
using namespace std;

int main ()
{
    int a,b,c,d,e,f,g,h,i,j;
    
    cout << "Enter ten numbers : ";
    cin >> a >> b >> c >> d >> e >> f >> g >> h >> i >> j;
    
    cout << "The sum is : " << a+b+c+d+e+f+g+h+i+j << endl;
    return 0;
}

